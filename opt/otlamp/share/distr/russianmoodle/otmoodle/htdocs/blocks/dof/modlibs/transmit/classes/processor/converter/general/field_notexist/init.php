/**
 * Обмен данных с внешними источниками. Поле не найдено.
 * 
 * Вернет статичную переменную, если указанное поле не найдено в пулле
 * 
 * @package    sync
 * @subpackage transmit
 * @license    http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */

class dof_modlib_transmit_processor_converter_general_field_notexist extends dof_modlib_transmit_processor_base
{
    /**
     * Обязательные входящие данные для запуска обработчика
     *
     * @var array
     */
    public static $slots_input = ['search_value' => null];
    
    /**
     * Статичные данные
     *
     * @var array
     */
    public static $slots_static = ['returnvalue' => null];
    
    /**
     * Исходящие данные
     *
     * @var array
     */
    public static $slots_output = ['returnvalue' => null];
    
    /**
     * Запуск обработчика
     *
     * @param array $input - Входящие данные
     * @param dof_control $dof - Контроллер Электронного Деканата
     * @param dof_storage_logs_queuetype_base $logger - Очередь логов
     * @param dof_modlib_transmit_source_filemanager $filemanager - Менеджер файлов
     *
     * @return array - Исходящие данные
     */
    public static function execute($input, $dof, $logger, $filemanager)
    {
        if ( ! array_key_exists('returnvalue', $input) )
        {// Поле не передано
            return [];
        }
        
        if ( ! array_key_exists('search_value', $input) )
        {// Поле не передано
            return ['returnvalue' => $input['returnvalue']];
        }
        return [];
    }
}